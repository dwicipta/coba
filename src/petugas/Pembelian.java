    public void aidi(){
         try{
            String sql = "select max(right(IDBarang,3)) from barang";
            Statement stat = con.createStatement();
            ResultSet rs= stat.executeQuery(sql);
            while(rs.next()){
                if(rs.first()==false){
                    id.setText("BRG001");
                } else {
                    rs.last();
                    int aut = rs.getInt(1) + 1;
                        String nomor = String.valueOf(aut);
                        int Long = nomor.length();
                        
                        for(int a=0;a<3-Long;a++){
                            nomor = "0" + nomor;
                        }
                        id.setText("BRG"+nomor);
                }
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "data gagal otomatis"+e);
        }

    }
    
