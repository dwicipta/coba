    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nama.getText().equals("") && hrg.getText().equals("") && ket.getText().equals("") && jml.getText().equals("") && pmsk.getText().equals("") && almt.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Data tidak boleh kosong !");
        }else{
        Connection con = koneksi.getConnection();
        String sql = "insert into pemasok values (?,?,?,?,?)";
        try {
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, pmsk.getText());
            stat.setString(2, almt.getText());
            stat.setString(3, nama.getText());
            stat.setString(4, jml.getText());
            stat.setString(5, tgl.getText());
            stat.executeUpdate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" +e);
        }
        String sqll = "insert into barang values (?,?,?,?,?,?)";
        String name = "select * from barang where Nama='"+nama.getText()+"'";
        try {
            PreparedStatement stat = con.prepareStatement(sqll);
            ResultSet rs= stat.executeQuery(name);
            if(rs.first()==false){
                    stat.setString(1, id.getText());
                    stat.setString(2, nama.getText());
                    stat.setString(3, (String) jns.getSelectedItem());
                    stat.setString(4, hrg.getText());
                    stat.setString(5, jml.getText());
                    stat.setString(6, ket.getText());
                    stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Sudah Disimpan");
            } else {
                JOptionPane.showMessageDialog(null, "Stok Sudah Ditambahkan");
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" +e);
        }

        kosong();
        aidi();
        tgl();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

