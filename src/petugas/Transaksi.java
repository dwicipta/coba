    private void mencariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mencariKeyPressed
        int key = evt.getKeyCode();
        int stok = 0;
        if(key == KeyEvent.VK_ENTER)
        if(mencari.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Tidak bisa mencari jika kosong !");
        }else{
            try{
                Statement stat = con.createStatement();
                String search = mencari.getText();
                ResultSet rs = stat.executeQuery("SELECT * FROM barang WHERE Nama like '%"+search+"%' OR Keterangan like '%"+search+"%' AND Stok !='"+stok+"'");
               while (rs.next()) {
                   
                    aidibar.setText(rs.getString("IDBarang"));
                    Nama.setText(rs.getString("Nama"));
                    harga.setText(rs.getString("Harga"));
                
                rs.close();
                mencari.setText("");   
               
               }
            }catch (Exception e){
            }
        }
    }//GEN-LAST:event_mencariKeyPressed

