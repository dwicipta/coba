    private void beliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliActionPerformed
        if(bayar.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bayar tidak boleh kosong !");
        }else{
            int a,b,c;
            a=Integer.parseInt(total.getText());
            b=Integer.parseInt(bayar.getText());
            c=b-a;
            kembali.setText(""+c);
            int bay, har;
            bay = Integer.parseInt(bayar.getText());
            har = Integer.parseInt(harga.getText());

            if(bay<har){
                JOptionPane.showMessageDialog(rootPane, "Harap bayar dengan uang pas atau lebih !");
            }else{
                
            String sql = "insert into detail values (?,?,?,?)";
                try {
                    PreparedStatement stat = con.prepareStatement(sql);
                    stat.setString(1, aidi.getText());
                    stat.setString(2, total.getText());
                    stat.setString(3, bayar.getText());
                    stat.setString(4, kembali.getText());
                    stat.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data Gagal Ditambah //" +e);
                }
            
                
                String id="";
                String sql2 = "select * from transaksi";
                try{
                    PreparedStatement stat = con.prepareStatement(sql2);
                    ResultSet rs = stat.executeQuery(sql2);
                    while(rs.next()){
                        id =rs.getString("IDTransaksi");
                    }
                }catch(Exception e){

                }

                JasperReport JasRep;
                JasperPrint JasPri;
                Map<String, Object> no = new HashMap<>();
                HashMap parameter = new HashMap();
                JasperDesign JasDes;
                try{
                   File report = new File("src/tubes/Trans.jrxml");
                   JasDes = JRXmlLoader.load(report);
                   no.clear();
                   parameter.put("trans",id);   
                   JasRep = JasperCompileManager.compileReport(JasDes);
                   JasPri = JasperFillManager.fillReport(JasRep, parameter, con);
                   JasperViewer.viewReport(JasPri, false);

               }catch(Exception e){
                   JOptionPane.showMessageDialog(null, e.getMessage());
               }
               aidi();
            }
        }
    }//GEN-LAST:event_beliActionPerformed

